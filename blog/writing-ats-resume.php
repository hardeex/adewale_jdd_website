               <h3 id="what-is-ats">what is <abbr title="Applicant Tracking System">Applicant Tracking System (ATS)</abbr>?</h3>
               <p>Applicant Tracking System (ATS) are application software that scans curriculum vitae or resumes and other job recruitment documents, 
                such as cover letter for relevant keyword, that is, words or terms that are specific to the job. The <abbr title="Applicant Tracking System">Applicant Tracking System (ATS)</abbr>
                 ATS scan your resume right from the career objective to the last part of your resume. When writing your resume, you should embed essential keywords in 
                 headings such as the career summary, educational background, skills, and job titles to increase your chance of employment.
               </p>
               <p>
                
        A common mistake is submitting the same resume for all job roles. Each job has keywords associated with it; each 
        employer have specific skills or values they are seeking. Before submitting a resume, ensure the essential keywords 
        from the job posting or advert are embedded in your resume. Read the job description carefully, find what the employer wants, the employer
         values and input them in your resume. Please, don't lie in your resume/CV but don't belittle yourself. </p> <br>




               <h3 id="how-does-ATS-works">how does <abbr title="Applicant Tracking System">APPLICANT TRACKING SYSTEM (ATS)</abbr> works?</h3>
               <img src="https://s3.amazonaws.com/resume.cdn/articles/posts/dc9c6a16f85d5db665059e36b0a2af10-9241196_atsinfographic.png" alt="how-does-ats-work image" class="ats-image" >
               <p>
                The <abbr title="Applicant Tracking System">ATS</abbr>  system categorizes a resume's content before scanning it for precise 
                and relevant keywords to decide whether or not the job application should be forwarded to the recruiter.
            It parses the resume into a category and scans the documents for relevant keywords to determine whether or not to pass 
            the documents to the recruiter. <br> It helps to disqualify candidates that do not fit the job description and choose the best 
            candidates from the pool of resume lists. 
            Each employment agency or employers submit certain keyword to the <abbr title="Applicant Tracking System">ATS</abbr> software. <br>
            The software searches for the keywords which are submitted in the recruiter database. Any resume not selected is truncated, that is not selected for the job interview. 
            This means that if a well-qualified candidate does not a write an <abbr title="Applicant Tracking System">ATS</abbr>  friendly resume, then he or she won't have a chance of 
            getting interviewed for the job
               </p> <br>



