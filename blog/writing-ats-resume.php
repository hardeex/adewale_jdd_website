               <h3 id="resume-styling">what styling should i use for my resume?</h3>
               <p>The  <abbr title=" Applicant Tracking System">ATS</abbr>  software is not designed to read graphics and images. 
                When writing your resume for  <abbr title=" Applicant Tracking System">ATS</abbr> , you should keep
                 this in mind. Choose fonts you can easily read and avoid any font colour other than black. 
                 A font size of 10 pt, 12 pt, or 13 pt is okay for paragraphs and 14 pt, 16 pt, or 18 pt for headings. </p>
               <p>
                Time News Roman and Arial are the best standout fonts for an  <abbr title=" Applicant Tracking System">ATS</abbr>  resume, but your other fonts, 
                like Cambria, Georgia, Calibri, and Verdana, are also proven to be  <abbr title=" Applicant Tracking System">ATS</abbr>-friendly.   
               </p>

