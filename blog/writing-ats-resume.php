               <h3 id="avoid-on-resume">what i should avoid on my resume to make it ATS friendly </h3>
               <p>
                A bad habit is copying a resume online or from a friend without in-depth proofreading. You can always seek 
                better ways to make your resume the best, but don't copy and paste even an expertly written resume. 
                It is your personal experience; it is unique.
               </p>
               <p>The following are some best practices for <abbr title=" Applicant Tracking System">ATS</abbr>  resume. 
                Ensure the following are in properly checked before submitting your resume</p>
               <ol>
                <li>avoid abbreviation
                   <p>write your words in full. For example, write Bachelor of science (B.Sci) instead of B.sci</p> 
                    <img src="https://www.jobscan.co/wp-content/uploads/CPA.png" alt="abbreviation image" class="ats-image" >
                </li>
                <li>
                   <p>Don't use a table on your resume; bullet your points. When bulleting your point, 
                    avoid using symbols or stars. Use a circle or square bullets for ordered list bullets. </p>  
                    <img src="https://www.jobscan.co/wp-content/uploads/bullet-points.png" alt="bullet-styling image" class="ats-image" >
                </li>
                <li>
                   <p> Avoid extra spaces between words. For example, the following words are interpreted separately by the 
                    <abbr title=" Applicant Tracking System">ATS</abbr> : project manager and project  manager. the latter has extra 
                    space between the product and the manager</p> 
                </li>
                <li> <p>Enter dates in the format MM/YYYY. for example 02/2023 </p> </li>
                <li>
                   <p>Save the file name with your first and last name to avoid your resume being truncated by the <abbr title=" Applicant Tracking System">ATS</abbr>. 
                    For example, save the file as jamiuyusuf.docx</p> 
                </li>
                <li>
                   <p>Avoid the header and footer. Don't put essential details in the header and/or footer of your resume.
                     You've probably guessed by now that there are various types of <abbr title=" Applicant Tracking System">ATS</abbr>
                      software. Not all of this software has the capability of parsing information in the header and footer of documents.
                       Information such as names, emails, phone numbers, links should be written outside the header and footer of 
                       your Word document resume. </p> 
                </li>
                <li><p>Avoid images, charts, and graphics. Some ats software tends to have issues parsing images, charts and graphics on resume </p> </li>
                <li>
                   <p>Optimize your resume using <a href="https://www.online-utility.org/text/analyzer.jsp" target="_blank" rel="noopener noreferrer">keyoword optimizer</a>. 
                    Use keywords relevant to the position or role for which you are applying.
                    If you're unsure of the best resume keywords, start by gathering three to five job descriptions that best describe the position
                     you want. <br> Then, put the job description into a free tool that measures word and phrase frequency to find the phrases that are
                      often used throughout the job description for the roles in which you're interested. Include the phrases in your CV if 
                      you meet the requirements or have the abilities listed in the job posting.</p> <br>
                    <p>Consider the frequency and placement of these essential keywords throughout your resume while building an 
                        <abbr title=" Applicant Tracking System">ATS</abbr>-optimized resume. Depending on how frequently a term appears 
                        in your resume (aim to include the term two to three times throughout your resume), some applicant tracking 
                        systems (ATS) will assess the strength of your skills, while others will assign an estimated amount of experience
                         for a given skill based on where it appears in the resume. You should optimize your resume for both systems
                          if you want it to be fully compatible with either <abbr title=" Applicant Tracking System">ATS</abbr>.</p> <br>
                <p>
                    Depending on how long you held the position, some applicant tracking systems will correlate the length of experience for 
                    the skill with your employment history. When you state that you handled Product Design for the firm at your previous employment 
                    and you worked there for five years, the <abbr title=" Applicant Tracking System">ATS</abbr> will infer that you have 
                    five years of SEO expertise from that position. The <abbr title=" Applicant Tracking System">ATS</abbr> scan give a skill 
                    six months' experience if it mentioned on its own, such as in a core-competencies section or professional profile. 
                    Because of this, it's crucial that you highlight your skills across the entirety of your resume rather than simply in the 
                    skill area at the beginning of the <abbr title=" Applicant Tracking System">ATS</abbr> resume template.
                </p>
                </li> <br>
                <li><p>Use a simple design template</p> </li>
                <li>
                   <p> Use a chronological resume format or style that focuses on work history and avoid the functional resume format that focuses on abilities</p></li>
                <li> avoid fluff
                    <p>Words like "passionate," "professional," "expert, specialized, creative and the like are perceived as marketing fluff by recruiters. 
                        Learn where and how to insert those words in your resume.
                    </p>
                </li>
                <li><p>Your previous job role's roles and responsibilities should speak more to achievement than tasking. 
                    What recruiters want to know is how you have used your skills to help the companies you've worked for, 
                    not what you were tasked with. </p> 
                </li>
