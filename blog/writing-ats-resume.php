               <h3 id="ats-resume-template">what is <abbr title=" APPLICANT TRACKING SYSTEM">ATS</abbr>  resume template?</h3>
               <p>
               Using the wrong template for a resume can result in your resume remaining in the software's black hole 
               for eternity. Whatever design template you choose for your resume, simplicity is the primary goal. 
               Second, the <abbr title=" Applicant Tracking System">ATS</abbr>  software prefers the chronological resume format. Unless you have numerous work experiences or 
               have worked on multiple projects, your resume should not be longer than two pages. Choosing a good resume template 
               can help you achieve this.
               </p> <br>
               
               
               
               <h3 id="resume-file-format"> what file format should i submit for ats </h3>
               <p>The majority of job applicants are accustomed to submitting documents in PDF format. 
                Nothing is wrong with this, but research has shown that the <abbr title=" Applicant Tracking System">ATS</abbr> 
                is not so friendly with this file format. Unless the PDF file format is listed among the file formats that 
                you can submit, it is best to submit the .doc or .docx format of your resume. <br> PDFs are best for preserving 
                your resume designs, but they are not the most <abbr title=" Applicant Tracking System">ATS</abbr>-friendly 
                file type because they are not compatible with all <abbr title=" Applicant Tracking System">ATS</abbr> 
                 software. So why not play it safe?
               </p> <br>



