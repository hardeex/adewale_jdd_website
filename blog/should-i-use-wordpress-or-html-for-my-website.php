<h3>When to Use HTML for my website development? </h3>
<p>You can use Html to design and develop your website when: </p> 
<ul>
    <li> The contents of your website won't be changing often.</li>
    <li>You want to have total control over your website. </li>
    <li> You have coding experience or have funds available for outsourcing.</li>
    <li>You have team member(s) with coding experience and can effectively maintain the website. </li>
</ul>

<h3>The Pros of Html Website... </h3>
<p> Some basic pros of using Html to design your website are:</p> 
<ul>
    <li> It is faster on the server than websites designed with WordPress.</li>
    <li> There is full control over the front end and backend of the website.</li>
    <li>Backing up is very less often when compared with WordPress because there are not often updates. </li>
    <li> It is more secure than the WordPress counterpart.</li>
</ul>

