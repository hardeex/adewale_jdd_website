 <h3>The discrepancy between a website, web app and mobile application for business?  </h3>
<p> Almost all business entities and individuals are faced with this question. There are several reasons for choosing one of these 
options and choosing the wrong one can cause a significant detriment to business growth. The applications on your android devices or iOS devices are mobile applications. The contents or information you accessed 
through browsers such as Chrome, Firefox, UC Browsers, Opera Mini, Safari etc. are either web apps or websites.</p> 


<h3> The discrepancies between web apps and websites </h3>
<p>Accessing data or information through browsers in most cases requires access to the internet unless in cases where 
    the websites or web app is locally hosted. And, in these cases, it can only be accessed on the device at which the 
    server is configured. </p> <br>
<p>  Most times web application is confused with websites. Although the major difference is hardly significant with the recent 
improvement in programming languages used for developing both web applications and websites.
One of the major bottlenecks that make both confused the same is because both require browsers as a medium of accessing 
the desired information from the internet. there is a need for the front and back 
end development which are written in similar programming languages. </p> <br>
  
<p>A website is built using HTML, CSS and the JavaScript programming language. Content Management systems such as WordPress have
 made the development of websites to be as extremely easy as possible at reduced cost and time. It is important to note that 
 the development of web applications goes beyond the scripting languages of HTML and CSS and programming languages such as 
 Javascript. </p>
<p> Depending on the functionalities and complexities of the web app, the programming language required for development may 
involve other programming languages such as PHP, Python and Javascript frameworks such as JQuery, React Native, Angular 
JS etc.</p> <br>
<p>For this reason, it is essential for business owners to have an absolute understanding and pictorial representation of 
the website or web app's short term and long term objectives. </p><br>
<p> With the recent responsiveness of modern websites, one way to distinguish between a website and a web application is that the
 whole operations or tasks of a web app are on a single screen. While the website uses different two or more screens to perform 
 basic tasks. A common example of a web app is Google Gmail, the whole operation you are performing on the platform is on 
 the same screen. <br> Enough of the serious talk, let's look at the differences between web apps and websites from another dimension. <br>
</p>

<h3>The discrepancy between business website and web app using an example. </h3>
<p>Another difference between web applications and websites depends on what the user can do. This illustration will elucidate 
the English better, </p> <br>
<p>I have a friend named Donald and he wants to know how to cook a stew. So he picked up his phone and type in Google, 
“How to cook a good stew”. Of course! A Series of links will be displayed to him and he can open any of the pages to read 
about how to cook a good stew. Donald with no access to the page backend, so he cannot make changes to any of the cooking 
procedures he didn't agree with on the screen. He can read what's on the screen but cannot add any input, not even his name, 
address, his cooking style, this is a typical example of the website.Donald can only read and have a better understanding of 
the subject matter in this case.
</p><br>
<p>  In a web application, Donald won't ust be able to read the cooking procedure but can add or remove some cooking
procedures or make whatever changes he wants based on the level of access given by the developer i.e. the contents the developer 
allows to change.</p> <br>
<p> If you can perform a simple task such as online chat, make an online payment, performs a search, fill a form etc. 
then it is a web app. Websites are static contThe setback of using user interaction to differentiate websites from web applications is that you can hardly find a website 
lately that doesn't allow some form of interaction.ent (just see it like a newspaper that you read for several reasons).</p> <br>
<strong> In summary, users can only read the content of websites but can manipulate data or the contents in a web application.</strong> <br><br>

<p> The setback of using user interaction to differentiate websites from web applications is that you can hardly find a website 
lately that doesn't allow some form of interaction.Don't be confused by this because recent websites have web app integrated into 
them that allows them to perform the function of interaction. A typical example of a website is a blog site and Facebook, Gmail, 
YouTube are typical examples of web applications.</p>

